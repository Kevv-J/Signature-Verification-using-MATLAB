    imshow(imgp2,'Parent',handles.axes6);
    Feat_Err = handles.Feat_Err
    Feat1
    Feat2
    flag = 1;
    for i=1:9
        if abs(Feat2(i)-Feat1(i)) > Feat_Err(i)
            flag = 0;
